import mongoose from "mongoose";

/*----------------------------------------------------------------------------*/
/* Filename                                                                   */
/*----------------------------------------------------------------------------*/
const User = mongoose.Schema({
    name:{
        type: String,
        required: true
    },
    email:{
        type: String,
        required: true
    },
    gender:{
        type: String,
        required: true
    }
})

/*----------------------------------------------------------------------------*/
/* EXPORTS                                                                    */
/*----------------------------------------------------------------------------*/

export default mongoose.model("Users", User)